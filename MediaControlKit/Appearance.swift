import UIKit

public class Appearance: NSObject {
    public class var greenColor: UIColor {
        return UIColor(red: 0.44, green: 0.70, blue: 0.02, alpha: 1.0)
    }
    
    public class var lightGrayColor: UIColor {
        return UIColor(red: 0.14, green: 0.14, blue: 0.14, alpha: 1.0)
    }
    
    public class var darkGrayColor: UIColor {
        return UIColor(red: 0.08, green: 0.08, blue: 0.08, alpha: 1.0)
    }
}
